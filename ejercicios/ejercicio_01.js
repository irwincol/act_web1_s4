// Tipo de función: Declaración de función
// Ejemplo: cuadrado(4) → 16


// 1. Calcular el cuadrado de un número
function cuadrado(numero) {
    return numero * numero;
}

// Ejemplo de uso
console.log(cuadrado(4)); // 16
console.log(cuadrado(7)); // 49
